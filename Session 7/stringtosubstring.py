s="Naroda Orthopedic Hospital"
#  65432109876543210987654321  -

'''
rules of String
1) index is always start form 0
2) there is three types start end jump
'''
print(s[3:25])
print(s[:20])
print(s[2:])
print(s[1:14:3])
print(s[::5])

print(s[-20:-5])
print(s[:-7])
print(s[-12:])
print(s[-23:-2:4])
print(s[::-3])
